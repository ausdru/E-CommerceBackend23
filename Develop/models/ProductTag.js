// Sequelize model for the 'product_tag' table with 
// columns id, product_id, and tag_id.


const { Model, DataTypes } = require('sequelize');

const sequelize = require('../config/connection');

const { INTEGER } = require('sequelize');

// -------------------------------------------------------------
// Definining product tag's columns.

class ProductTag extends Model {}

ProductTag.init(
  {
    id: {
      type: DataTypes.INTEGER,
      allowNull: false,
      primaryKey: true,
      autoIncrement: true
    },
    product_id: {
      type: DataTypes.INTEGER,
      references: {
        model: 'product',
        key: 'id'
      }
    },
    tag_id: {
      type: DataTypes.INTEGER,
      references: {
        model: 'tag',
        key: 'id'
      }
    }
  },
  {
    sequelize,
    timestamps: false,
    freezeTableName: true,
    underscored: true,
    modelName: 'product_tag',
  }
);

module.exports = ProductTag;
